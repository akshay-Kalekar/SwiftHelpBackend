const mongoose = require('mongoose');

const bloodDonationRequestSchema = new mongoose.Schema({
  requester: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  contactNo: {
    type: String,
    required: true,
  },
  bloodGrp: {
    type: String,
    enum: ['A+', 'A-', 'B+', 'B-', 'O+', 'O-', 'AB+', 'AB-'],
    required: true,
  },
  medicalHistory: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zipCode: {
    type: String,
    required: true,
  },
});

const BloodDonationRequest = mongoose.model('BloodDonationRequest', bloodDonationRequestSchema);

module.exports = BloodDonationRequest;
